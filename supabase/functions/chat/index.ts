import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are MeYone Vault Assistant, a helpful AI chatbot for a secure document vault platform in Sierra Leone. You help users with:

1. **Document Guidance**: Explain how to upload, encrypt, and manage documents (birth certificates, academic degrees, property deeds, etc.)
2. **Verification Help**: Guide users on how to verify document authenticity using QR codes or hash verification
3. **Platform Features**: Explain features like secure sharing, next-of-kin access, institutional document management
4. **FAQ Automation**: Answer common questions about the platform
5. **Security Information**: Explain encryption (AES-256), hash verification (SHA-256), and privacy features

Key facts about MeYone Vault:
- Documents are encrypted client-side with AES-256 before upload
- Each document gets a unique QR code and verification link
- SHA-256 hashes ensure document authenticity
- Institutions (MDAs, universities, hospitals) can issue and verify documents
- Next-of-kin can access documents after 12 months of inactivity
- The platform uses Sierra Leone's national colors (green, white, blue)

Be helpful, concise, and reassuring about document security. If you don't know something, say so.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add credits to continue." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "AI service unavailable" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Chat function error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
